/** 365 mots tournants pour garantir un mot quotidien sur toute l'année. */
export const MOT_HIER_WORDS: readonly string[] = Array.from({ length: 365 }, (_, index) => {
  return BASE_WORDS[index % BASE_WORDS.length]!;
});

export function getMotDuJourForDateKey(dateKey: string): string {
  const n = MOT_HIER_WORDS.length;
  if (n === 0) return "mot";
  const i = dayOfYearFromDateKey(dateKey) % n;
  return MOT_HIER_WORDS[i]!;
}
